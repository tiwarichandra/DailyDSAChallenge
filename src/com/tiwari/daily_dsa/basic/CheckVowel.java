package com.tiwari.daily_dsa.basic;

public class CheckVowel {
    public static void main(String[] args) {
        char ch = 'E';

        if (ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U') {
            System.out.println("YES");
        } else {
            System.out.println("No");
        }

    }
}
